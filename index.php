    // Set today's date in the hidden fields of both modals
    document.addEventListener("DOMContentLoaded", function () {
        const today = new Date().toISOString().split('T')[0];
        const dateInputs = ['dates-meeting', 'dates-booking'];
        dateInputs.forEach(id => {
            const el = document.getElementById(id);
            if (el) el.value = today;
        });
    });

    // Form submission handler for meeting modal
    var meetingForm = document.getElementById('modal-meeting-form');
    if (meetingForm) {
        meetingForm.addEventListener("submit", e => {
            e.preventDefault();
            fetch(meetingForm.action, {
                method: "POST",
                body: new FormData(meetingForm),
            })
            .then(response => response.json())
            .then(data => {
                swal("Submit Successfully!", "Meeting Schedule Soon!", "success");
            })
            .catch(error => {
                swal("Error", "Something went wrong. Please try again.", "error");
            });
        });
    }

    // Form submission handler for booking modal
    var bookingForm = document.getElementById('modal-booking-form');
    if (bookingForm) {
